-- ........................... Question 1 : Creation de la table EMP & DEPT ........................... --
CREATE TABLE DEPT(
    DEPTNO INTEGER(7) NOT NULL PRIMARY KEY,
    DNAME VARCHAR(25),
    LOC VARCHAR(25)
);

CREATE TABLE EMP(
    EMPNO INTEGER(7) NOT NULL PRIMARY KEY,
    ENAME VARCHAR(25),
    JOB VARCHAR(25),
    MGR INTEGER(7),
    HIREDATE DATE,
    SAL INTEGER(9.2),
    COMM INTEGER(9.2),
    DEPTNO INTEGER(7),
    FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO)
);

